package Coupon_Purchasing_System;

public class Report {
    private String salesReport;
    private String customersPurchase;
    private String inventoryReport;

    // Getters and Setters
    public String getSalesReport() {
        return salesReport;
    }

    public void setSalesReport(String salesReport) {
        this.salesReport = salesReport;
    }

    public String getCustomersPurchase() {
        return customersPurchase;
    }

    public void setCustomersPurchase(String customersPurchase) {
        this.customersPurchase = customersPurchase;
    }

    public String getInventoryReport() {
        return inventoryReport;
    }

    public void setInventoryReport(String inventoryReport) {
        this.inventoryReport = inventoryReport;
    }
}
